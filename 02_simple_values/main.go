package main

import "fmt"

func main() {
	// Integer
    age := 25
    fmt.Println("Age:", age)

    // Float
    pi := 3.14
    fmt.Println("Pi:", pi)

    // String
    name := "Go Language"
    fmt.Println("Name:", name)

    // Boolean
    isActive := true
    fmt.Println("Active:", isActive)

    // Arithmetic
    fmt.Println("1 + 1 =", 1+1)

}